package monstres_array;

public class Node {
	private Monster monsterAssocie;
	private Node next;


	public Monster getMonsterAssocie() {
		return monsterAssocie;
	}

	public void setMonsterAssocie(Monster monsterAssocie) {
		this.monsterAssocie = monsterAssocie;
	}

	public Node getNext() {
		return next;
	}

	public void setNext(Node next) {
		this.next = next;
	}


}
